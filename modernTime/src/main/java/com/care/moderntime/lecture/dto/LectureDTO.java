package com.care.moderntime.lecture.dto;

public class LectureDTO {

	private String id;
	private String practice;
	private String project;
	private String grade;
	private String attend;
	private String exam;
	private int score;
	private String comment;
	private String listen_date;
	private String lecture_id;
	private String user_id;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPractice() {
		return practice;
	}
	public void setPractice(String practice) {
		this.practice = practice;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getAttend() {
		return attend;
	}
	public void setAttend(String attend) {
		this.attend = attend;
	}
	public String getExam() {
		return exam;
	}
	public void setExam(String exam) {
		this.exam = exam;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getListen_date() {
		return listen_date;
	}
	public void setListen_date(String listen_date) {
		this.listen_date = listen_date;
	}
	public String getLecture_lecture_id() {
		return lecture_id;
	}
	public void setLecture_lecture_id(String lecture_lecture_id) {
		this.lecture_id = lecture_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}

}
